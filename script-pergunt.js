function toggleDelt1Visibility() {
    var t1 = document.getElementById("tri1");
    var t2 = document.getElementById("tri2");
    var r1 = document.getElementById("resp1");
    var p1 = document.getElementById("per1");

    if (t1.style.display === "none") {
        t1.style.display = "inline";
        t2.style.display = "none";
        r1.style.display = "none";
        p1.style.color = "var(--corfonte)";
    } else {
        t1.style.display = "none";
        t2.style.display = "inline";
        r1.style.display = "inline";
        p1.style.color = "gray";
    }
}

